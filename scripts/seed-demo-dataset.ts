async function ensureClients(pool: Pool, entityId: string): Promise<number> {
  let inserted = 0;
  for (const c of MOCK_CLIENTS) {
    const res = await pool.query(
      `INSERT INTO clients (id, name, type, segment, rating, country, entity_id)
       VALUES ($1, $2, $3, $4, $5, 'ES', $6::uuid)
       ON CONFLICT (id) DO NOTHING`,
      [c.id, c.name, c.type, c.segment, c.rating, entityId],
    );
    inserted += res.rowCount ?? 0;
  }
  return inserted;
}

