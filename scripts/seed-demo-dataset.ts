#!/usr/bin/env tsx
/**
 * Comprehensive demo dataset seed (Phase 7 — unified demo catalogue).
 *
 * Populates the live Postgres database with a coherent demo dataset under
 * `DEFAULT_ENTITY_ID` so that:
 *
 *   1. Toggling "Demo" mode in the UI points to real DB rows (not in-memory
 *      JS constants). Requires the PR B refactor of DataContext to remove
 *      `enabled: dataMode === 'live'` guards before it takes full effect.
 *   2. Every Phase 6 surface (Clients, CLV tabs, Pipeline, Reconciliation)
 *      shows consistent data for the same set of clients — CL-1001 has
 *      positions AND metrics AND events AND a LTV snapshot AND NBA rows,
 *      not a fragmented subset.
 *   3. The seed is idempotent: `ON CONFLICT DO NOTHING` or append-safe on
 *      every INSERT. Re-running is cheap (only missing rows get written).
 *
 * Scope covered:
 *   - Core reference data: clients, products, business units, rules
 *   - Phase 6 Customer 360: client_positions, client_metrics_snapshots,
 *     client_events, client_ltv_snapshots, client_nba_recommendations
 *
 * Out of scope (intentionally):
 *   - pricing_snapshots — these are generated by running the pricing
 *     engine, not seeded directly. The Reconciliation view derives
 *     BU↔Treasury pairs from real snapshots; to populate it, run the
 *     engine against seeded deals (follow-up worker PR).
 *   - deals themselves — MOCK_DEALS in the JS catalogue already covers
 *     these in demo mode; server routes/deals.ts reads from the `deals`
 *     table when live. Left as follow-up so this script stays read-safe.
 *
 * Usage:
 *   DATABASE_URL=postgres://... npx tsx scripts/seed-demo-dataset.ts
 *   DATABASE_URL=postgres://... npx tsx scripts/seed-demo-dataset.ts --reset
 *   DATABASE_URL=postgres://... npx tsx scripts/seed-demo-dataset.ts --dry-run
 *
 * Exit 0 on success, 1 on error.
 */

